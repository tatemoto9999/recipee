session_start();
// ログイン状態チェック
if (!isset($_SESSION["USER_ID"])) {
    header("Location: Logout.php");
    exit;
}

$userId = $_SESSION["USER_ID"];
$name = $_SESSION["NAME"];

//1.  DB接続します xxxにDB名を入れます
try {
$pdo = new PDO('mysql:dbname=Tatemoto_19;charset=utf8;host=localhost','root','root');
} catch (PDOException $e) {
  exit('データベースに接続できませんでした。'.$e->getMessage());
}

//２．データ登録SQL作成
//作ったテーブル名を書く場所  xxxにテーブル名を入れます
$stmt = $pdo->prepare(
	"SELECT rec.id, rec.indate, rec.food, rec.upfile 
	FROM recipee_table as rec
	inner join tb_recipe_relation as rel
	on (rec.id = rel.recipe_id and rel.user_id = :userId)");
$stmt->bindValue(":userId", $userId, PDO::PARAM_INT);
$status = $stmt->execute();

//３．データ表示
$view="";
if($status==false){
  //execute（SQL実行時にエラーがある場合）
  $error = $stmt->errorInfo();
  exit("ErrorQuery:".$error[2]);
}else{
  //Selectデータの数だけ自動でループしてくれる $resultの中に「カラム名」が入ってくるのでそれを表示させる例
  while( $result = $stmt->fetch(PDO::FETCH_ASSOC)){
    $view .= "<p>";
    //$view .= $result["id"].":".$result["name"].$result["email"];
    $view .= '<a href ="u_view.php?id='.$result["id"].'">';
	$view .= $result["id"].$result["indate"].":".$result["food"];
	$view .= "<img src='./files/".$result["upfile"]."' class = 'food_sub_image'>";
    $view .='</a>';
    //.= 変数の前に文字が入っていた場合。データが上書きせずに一覧で表示される
    $view .= "</p>";
  }
}
